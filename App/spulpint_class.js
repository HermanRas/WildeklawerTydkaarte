class spilpunt {
    constructor(id, naam, afkorting, plaas_id) {
        this.id = id;
        this.naam = naam;
        this.afkorting = afkorting;
        this.plaas_id = plaas_id;
    }

    id() {
        return this.id;
    }

    naam() {
        return this.naam;
    }

    afkorting() {
        return this.afkorting;
    }

    plaas_id() {
        return this.plaas_id;
    }

}